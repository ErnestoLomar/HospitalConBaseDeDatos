    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        modelo.setRowCount(0);
        String datos[] = new String[6];
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("HospitalBDPU"); // Comprobar nombre de la PU
        EntityManager em = emf.createEntityManager();
        Query queryContactos = em.createNamedQuery("Paciente.findAll");
        List<Paciente> listContactos = queryContactos.getResultList();
        listContactos.forEach((Paciente) -> {
            datos[0]=String.valueOf(Paciente.getClave());
            datos[1]=Paciente.getNombre();
            datos[2]=Paciente.getAppaterno();
            datos[3]=Paciente.getApmaterno();
            datos[4]=Paciente.getEnfermedad();
            datos[5]=Paciente.getMedicamento();

            modelo.addRow(datos);
        });
        
        em.close();
        emf.close();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("HospitalBDPU"); // Comprobar nombre de la PU
        EntityManager em = emf.createEntityManager();
        clave = Integer.parseInt(jTextField4.getText());
        String medicamento="";
        String nombre = jTextField1.getText();
        String apaterno = jTextField2.getText();
        String amaterno = jTextField3.getText();
        String enfermedad = jTextField10.getText();
        if(enfermedad.equals("gripa")){
            medicamento = "Agrifen";
        }if(enfermedad.equals("tos")){
            medicamento = "Bisolvon";
        }

        Paciente paciente = new Paciente(clave, nombre,apaterno,amaterno,enfermedad,medicamento);

        em.getTransaction().begin();
        em.persist(paciente);
        em.getTransaction().commit();
        
        String datos[] = new String[6];
        Query queryContactos = em.createNamedQuery("Paciente.findAll");
        List<Paciente> listContactos = queryContactos.getResultList();
        listContactos.forEach((Paciente) -> {
            datos[0]=String.valueOf(Paciente.getClave());
            datos[1]=Paciente.getNombre();
            datos[2]=Paciente.getAppaterno();
            datos[3]=Paciente.getApmaterno();
            datos[4]=Paciente.getEnfermedad();
            datos[5]=Paciente.getMedicamento();

            modelo.addRow(datos);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField10.setText("");
        });

        em.close();
        emf.close();
        
        JOptionPane.showMessageDialog(null,"Paciente Agregado");
    }//GEN-LAST:event_jButton1ActionPerformed

