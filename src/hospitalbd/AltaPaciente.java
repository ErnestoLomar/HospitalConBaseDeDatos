    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("HospitalBDPU"); // Comprobar nombre de la PU
        EntityManager em = emf.createEntityManager();
        
        Paciente p1 = em.find(Paciente.class, clave);

        em.getTransaction().begin();
        p1.setNombre(jTextField6.getText());
        p1.setAppaterno(jTextField7.getText());
        p1.setApmaterno(jTextField8.getText());
        p1.setEnfermedad(jTextField9.getText());
        em.getTransaction().commit();
  
        
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField8.setText("");
        jTextField9.setText("");
        if(p1.getEnfermedad().equals("gripa")){
            p1.setMedicamento("Agrifen");
        }if(p1.getEnfermedad().equals("tos")){
            p1.setMedicamento("Bisolvon");
        }
        String datos[] = new String[6];
        Query queryContactos = em.createNamedQuery("Paciente.findAll");
        List<Paciente> listContactos = queryContactos.getResultList();
        listContactos.forEach((Paciente) -> {
            datos[0]=String.valueOf(Paciente.getClave());
            datos[1]=Paciente.getNombre();
            datos[2]=Paciente.getAppaterno();
            datos[3]=Paciente.getApmaterno();
            datos[4]=Paciente.getEnfermedad();
            datos[5]=Paciente.getMedicamento();
            modelo.removeRow(0);
            modelo.addRow(datos);
            
            
        });
        JOptionPane.showMessageDialog(null,"Datos modificados");
        
        em.close();
        emf.close();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("HospitalBDPU"); // Comprobar nombre de la PU
        EntityManager em = emf.createEntityManager();
        int id = Integer.parseInt(jTextField11.getText());
        Paciente p2 = em.find(Paciente.class, id);
        em.getTransaction().begin();
        em.remove(p2);
        em.getTransaction().commit();
        jTextField11.setText("");
        modelo.removeRow(0);
        JOptionPane.showMessageDialog(null,"Datos borrados");
        
        em.close();
        emf.close();
    }//GEN-LAST:event_jButton5ActionPerformed

